
import axios from 'axios'
const dynamicRoutes = () => {
  const routes = axios
    .get('https://admin.rocketjump.top/wp-json/wp/v2/posts?page=1&per_page=20')
    .then((res) => {
      return res.data.map(post => `/blog/${post.slug}`)
    })
  console.log(routes)
  return routes
}

